    private void oneStar(String input) {
        int[] instructions = inputToIntArray(input);
        int steps = 0;
        int index = 0;
        while (index >= 0 && index < instructions.length) {
            int instruction = instructions[index];
            instructions[index]++;
            index += instruction;
            steps++;
        }
        System.out.println(steps);
    }

    private void secondStar(String input) {
        int[] instructions = inputToIntArray(input);
        int steps = 0;
        int index = 0;
        while (index >= 0 && index < instructions.length) {
            int instruction = instructions[index];
            if (instruction >= 3) {
                instructions[index]--;
            } else {
                instructions[index]++;
            }
            index += instruction;
            steps++;
        }
        System.out.println(steps);
    }

    private int[] inputToIntArray(String input) {
        String[] digits = input.split("\n");
        int[] instructions = new int[digits.length];
        for (int i = 0; i < digits.length; i++) {
            int digit = Integer.parseInt(digits[i]);
            instructions[i] = digit;
        }
        return instructions;
    }
